function cubeOfNumber() {
  for (let number = 1; number <= 5; number++) {
    cube = number * number * number;
    console.log(number + " and cube of the " + number + " is: " + cube);
  }
}
cubeOfNumber();

function MultiplicationTable() {
  for (let i = 1; i <= 10; i++) {
    console.log("15 * " + i + " = " + 15 * i);
  }
}
MultiplicationTable();
